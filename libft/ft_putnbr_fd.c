#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	unsigned int un;

	un = (n < 0) ? -n : n;
	(n < 0) ? write(fd, "-", 1) : 0;
	(un > 9) ? ft_putnbr_fd(un / 10, fd) : 0;
	write(fd, &"0123456789"[un % 10], 1);
}
